#include "ft_printf.h"

char	*ft_strcpy(char *dest, char *src)
{
	int rt;

	rt = 0;
	while (*(src + rt) != '\0')
	{
		*(dest + rt) = *(src + rt);
		rt++;
	}
	*(dest + rt) = '\0';
	return (dest);
}
